"""
    wick(G::Matrix{<:Number}, indices::Int...)

# Description
The function calculating expected value of observable by Wick theorem.

# Arguments
- `G::Matrix{<:Number}`: Green function G_ij = ⟨ϕ|c^dag_i c_j|φ⟩ = I - ⟨ϕ|c_i c^dag_j|φ⟩
- `indices::Int...`: the indices corresponding to the base operators

# Return
- `::Number`: expected value of observable

# Warning
wick(G::Matrix{<:Number}, indices::Int...) provides a general `wick` method.
However, due to the large number of the built-in function `_permutation_sign` called,
the efficiency of calculating the expectation of n-body operator with it decreases rapidly with n.
It is recommended to use `_auto_wick_text` to generate the required `wick` method.
"""
function wick(G::Matrix{<:Number}, indices::Int...)
    @warn "The general `wick` method called is severely underperforming because of the frequent use of `_permutation_sign`!"
    @warn "It is recommended to use `_auto_wick_text` in `src/action/wick.jl` to generate the corresponding `wick` method."
    @assert mod(length(indices), 2) == 0 "The observable is beyond the measurable range of Slater's determinant space"
    half_n = div(length(indices), 2)
    index_index_permutation = collect(permutations(1:half_n))
    index = indices[half_n+1:end]
    return sum(x->_permutation_sign(x)*prod(y->G[indices[y], index[x[y]]], 1:half_n), index_index_permutation)
end
function _permutation_sign(p)
    n = length(p)
    perm_matrix = zeros(Int, n, n)
    for (i, val) in enumerate(p)
        perm_matrix[i, val] = 1
    end
    return det(perm_matrix) == 1 ? 1 : -1
end

"""
    function wick(G::Matrix{<:Number}, dict::Dict{<:Tuple{Vararg{Int64}}, <:Number})

# Arguments
- `G::Matrix{<:Number}`: Green function G_ij = ⟨ϕ|c^dag_i c_j|φ⟩ = I - ⟨ϕ|c_i c^dag_j|φ⟩
- `dict::Dict{<:Tuple{Vararg{Int64}}, <:Number})`: the `Dict` instance converted from a many-body operator by `Op2Dict`

# Return
- `::Number`: expected value of observable
"""
function wick(G::Matrix{<:Number}, dict::Dict{<:Tuple{Vararg{Int64}}, <:Number})
    return sum([value*wick(G, key...) for (key, value) in dict])
end


# automatically generate the text of `wick` for n-body operator
function _auto_wick_text(n::Int)
    @assert n > 0 "n must be a positive integer!"
    strs = String[]

    str0 = "function wick(G::Matrix{<:Number}"
    for i in 1:n
        str0 *= ", i$(i)::Int"
    end
    for i in 1:n
        str0 *= ", j$(i)::Int"
    end
    str0 *= ")"
    push!(strs, str0)

    for p in collect(permutations(1:n))
        str = "          "

        if _permutation_sign(p) == 1
            str *= "+ "
        elseif _permutation_sign(p) == -1
            str *= "- "
        else
            throw(ErrorException("Errors that exceed expectations"))
        end

        for i in 1:n
            str *= "G[i$(n+1-i),j$(p[i])] * "
        end

        push!(strs, str[1:end-3])
    end
    if strs[2][11] == '+'
        strs[2] = "    return (" * strs[2][13:end]
    else
        strs[2] = "  return (" * strs[2][11:end]
    end
    strs[end] *= ")"

    push!(strs, "end")

    f = open("output.txt", "w")
    for s in strs
        println(f, s)
    end
    close(f)
end

